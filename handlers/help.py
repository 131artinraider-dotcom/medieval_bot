# ========================================
# بستن پنل راهنما
# ========================================
async def help_close(update: Update, context: ContextTypes.DEFAULT_TYPE):
    """بستن پنل راهنما"""
    query = update.callback_query
    await query.answer()
    await query.delete_message()

